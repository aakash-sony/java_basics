/*
Q* WAP to add two numbers without using 3rd variable.
----------------------------------------------
*/
public class AddWithoutThirdVar
{
	public static void main(String []args)
       {
          int x = 10;
          int y = 20;
       
          System.out.println("Sum is: " +x+y);
          System.out.println(+x+y);
          System.out.println("" +x+y);
          System.out.println("Sum is: " +(x+y));
          
        }
}

