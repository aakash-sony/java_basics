/*
Write a program in Java to display the following pattern on the screen without using loop.
@@@@@@@@
@      @
@      @
@      @
@      @
@      @
@@@@@@@@
-------------------------------------------------------------------------------------------
*/
public class PatternPrinting
{
     public static void main(String args[])
     {
         System.out.println("@@@@@@@@");
         System.out.println("@"+"      "+"@");
         System.out.println("@"+"      "+"@");
         System.out.println("@"+"      "+"@");
         System.out.println("@"+"      "+"@");
         System.out.println("@"+"      "+"@");
         System.out.println("@@@@@@@@");
     }

}