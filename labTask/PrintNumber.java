public class PrintNumber {
	public static void printn(int n) {
		System.out.println("In Integer: "+n);
	}

	public static void printn(long n) {
		System.out.println("In Long: "+n);
	}

	public static void printn(double n) {
		System.out.println("In Double: "+n);
	}

	public static void printn(float n) {
		System.out.println("In float: "+n);
	}

	public static void printn(char c) {
		System.out.println("In character: "+c);
	}

	public static void printn(String str) {
		System.out.println("In String: "+str);
	}
}
